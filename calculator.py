#python program to build a calculator 

#taking two numbers as input from user 
n1 = int(input("Enter first number : "))
n2 = int(input("Enter second number : ")) 

#logic for building a calculator 
print("The addition of above numbers is : ", n1+n2)
print("The subtraction of above numbers is : ", n1-n2)
print("The multiplication of above numbers is : ", n1*n2)
print("The division of above numbers is : ", n1/n2)
print("The floor division of above numbers is : ", n1//n2)
print("The modulus of above numbers is : ", n1%n2)
print("The exponent of above numbers is : ", n1**n2)